//Write Below Pattern Using Nested Loop.
           //75 76 77 78 79

           //76 77 78 79

           //77 78 79

           //78 79

            //79
#include<stdio.h>
#include<conio.h>
int main()
{
	int i,j;
	for(i=75;i<=79;i++)
	{
		for(j=i;j<=79;j++)
		{
			printf("%d ",j);
		}
		printf("\n");
	}
  }  
